    {
        $event->load(['fights.redFighter', 'fights.blueFighter']);
        $event->fights = $event->fights->sortBy('match_order');
        $predicts = Predict::groupByFightAndFighter($event->pluck('id'));
        return view('event.show', compact('event', 'predicts'));
    }

